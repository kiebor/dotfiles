-- Enable syntaxic coloration
vim.opt.syntax = on

-- Enable line numbers
vim.opt.number = true

-- Set tabs to 3 characters and use space instead of tabs
vim.opt.tabstop = 3
vim.opt.expandtab = true

-- Use a block instead of a caret
vim.opt.guicursor = 'i:block'

-- Set default colorscheme to tokyonight-night
vim.cmd.colorscheme('tokyonight-night')

-- Set background to dark
vim.opt.background = 'dark'

-- Disable mouse support
vim.opt.mouse = ''
